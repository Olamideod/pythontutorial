# Floating point are numbers with decimal value

your_float = input("Enter floating point: ")

# We can convert a string into a floating point
your_float = float(your_float)

# We can decide how many decimal point we want our float to have e.g {:.2f} telling our float to print with 2 decimal point

print("Rounding up to 3 decimal places : {:.3f}".format(your_float))
